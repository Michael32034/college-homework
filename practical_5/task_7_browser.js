let x = parseInt(prompt("Введіть перше число: "));
let y = parseInt(prompt("Ввведіть друге число: "));

console.log("Результати:")
console.log(`${x} & ${y} = ${x & y}`);
console.log(`${x} | ${y} = ${x | y}`);
console.log(`${x} ^ ${y} = ${x ^ y}`);
console.log(`${x} << 1 = ${x << 1}`);
console.log(`${x} >> 1 = ${x >> 1}`);
